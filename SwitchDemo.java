class SwitchDemo
{
public static void main(String arg[])
{
char ch;
ch='A';
switch(ch)
{
case'A':
System.out.println("Vowel");
break;
case'E':
System.out.println("Vowel");
break;
case'I':
System.out.println("Vowel");
break;
case'O':
System.out.println("Vowel");
break;
case'U':
System.out.println("Vowel");
break;
default:
System.out.println("Consonant");
}
}
}
