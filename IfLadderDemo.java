class IfLadderDemo
{
public static void main(String arg[])
{
char ch;
ch='A';
if(ch=='A')
System.out.println("Vowel");
else if(ch=='E')
System.out.println("vowel");
else if(ch=='I')
System.out.println("vowel");
else if(ch=='O')
System.out.println("vowel");
else if(ch=='U')
system.out.println("Vowel");
else
system.out.println("Consonant");
}
}
